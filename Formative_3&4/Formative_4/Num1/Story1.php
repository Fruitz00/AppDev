    <h1> The Boy and the Whirlwind </h1>
<p>
    In Old Mondstadt transpired the story to be told,<br>
    Where a tyrant ruled, I met a boy, not that old.<br>
    The lyre he played, and for a song he sought,<br>
    But storm-walls blocked blue sky — he was sincerely distraught.<br>

    "I do so wish to see the birds in flight"<br>
    Said he, his strong eyes filling with light.<br>
    But his voice was lost in the howling wind's churn.<br>
    For the whirlwind takes, and gives naught in return.<br>
    The true sky, and songs that cageless soar...<br>
    Were they not wishes worth fighting for?<br>
    So the boy turned, extending his hand:<br>
    "Let us cast down the tyrant and his walls from this land."<br>
    The young boy raised then the flag of revolt,<br>
    And I threw myself into freedom's tumult.<br>
    Victorious were we who fought to be free.<br>
    Gods fell, winds whipped, nations shook violently.<br>
    In the smoke, a despot met his doom,<br>
    And we watched as his great tower fell none too soon.<br>
    Mondstadt began anew, the story passed down—<br>
    And since then never has another worn its crown.<br>
