    <h1> Yakshas: The Guardian Adepti </h1>
<p>
In ancient times, Liyue was a land of misery, where the shadow of evil loomed large. <br>
As slain gods festered, their vengeful wrath cursed the world, manifesting in infernal forms.<br>
When demons stirred—miasmas, monsters, and mutations infested the land.<br>
Then, rex lapis summoned the yakshas to vanquish the demons. they swore an oath:<br>

“Restore order through slaughter, purge evil through battle; to this, we dedicate our lives.”<br>
Eons of bloodshed later, karmic debt weighed upon them,<br>
Phantom wrath seeping into their broken souls.<br>
They went mad with fear, turned on each other, or succumbed to the darkness.<br>
Of the five foremost yakshas, death came to three, while the fourth vanished without a trace.<br>
In the millennia since, one conqueror of demons remains the sole surviving yaksha in the mortal realm.<br>
And only on moonlit nights, in the glow from Guyun and in the sound of the Dihua flute, is his memory preserved.
